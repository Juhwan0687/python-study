import turtle
t=turtle.Turtle('turtle')
t.speed(0)
t.setx(-200)
t.write('0')
t.setx(0)
t.write('50')
t.setx(200)
t.write('100')
t.begin_fill()
t.fillcolor('yellow')
t.setx(320)
t.right(90)
t.sety(100)
t.setx(220)
t.sety(0)
t.end_fill()
t.backward(100)
t.begin_fill()
t.fillcolor('red')
t.goto(270,200)
t.goto(320,100)
t.end_fill()
t.penup()
t.goto(20,200)
t.left(90)
t.write('김주환의 타자 게임!', font=('Arial',20))
t.goto(0,250)
turtle.done()